    "\n",
    "def interference_score_for_bin(bin_index: int, t: float, beacon_channel: int, decoy_channel: int, level: int) -> int:\n",
    "    \"\"\"\n",
    "    Python version of the game idea:\n",
    "    direct wave + reflected wave + optional decoy/twin wave.\n",
    "    t is a phase-like time value in radians.\n",
    "    \"\"\"\n",
    "    if not interference_active(level):\n",
    "        return 68\n",
    "    f = flags_for_level(level)\n",
    "    b = channel_to_bin(beacon_channel)\n",
    "    d = channel_to_bin(decoy_channel)\n",
    "    x = float(bin_index)\n",
    "    dx = x - float(b)\n",
    "    dd = x - float(d)\n",
    "\n",
    "    direct = math.cos(t + dx * 1.17) / (1.00 + 0.22 * abs(dx))\n",
    "    reflect = 0.64 * math.cos(t * 0.82 - dx * 1.63 + level * 0.37) / (1.25 + 0.14 * abs(dx + 2.0))\n",
    "\n",
    "    decoy = 0.0\n",
    "    if f.ghost or f.twin or f.ble:\n",
    "        decoy = 0.46 * math.cos(t * 1.31 + dd * 1.41 + 0.8) / (1.15 + 0.18 * abs(dd))\n",
    "\n",
    "    amp = abs(direct + reflect + decoy)\n",
    "    score = int(amp * 62.0 + 18.0)\n",
    "    if bin_index == channel_to_bin(beacon_channel):\n",
    "        score += 10\n",
    "    if bin_index == channel_to_bin(decoy_channel) and f.ghost:\n",
    "        score += 6\n",
    "    return int(np.clip(score, 0, 100))\n",
    "\n",
