    "## 7. Filter mechanic: Fourier/Laplace without making the player do equations\n",
    "\n",
    "The player sees only:\n",
    "\n",
    "```text\n",
    "FILT FAST / MED / SLOW\n",
    "```\n",
    "\n",
    "But underneath, this is a very good educational metaphor:\n",
    "\n",
    "| Filter | Game behavior | Signal-processing idea |\n",
    "|---|---|---|\n",
    "| FAST | reacts quickly, good for hopping signals | broader bandwidth / faster time response |\n",
    "| MED | reliable default | matched-filter style compromise |\n",
    "| SLOW | stable in noisy levels | smoothing / low-pass response |\n",
    "\n",
    "Fourier intuition:\n",
    "\n",
    "- A fast-changing signal needs a filter that can follow changes.\n",
    "- A noisy environment benefits from narrower/smoother filtering.\n",
    "\n",
    "Laplace/control intuition:\n",
    "\n",
    "A simple first-order smoothing system can be modeled as:\n",
    "\n",
    "$$\n",
    "H(s)=\\frac{1}{\\tau s + 1}\n",
    "$$\n",
    "\n",
    "- small `tau` → fast but jittery\n",
    "- large `tau` → slow but stable"
