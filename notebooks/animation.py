    "def make_signal(n=400, hopping=False, noise=0.3, seed=0):\n",
    "    rng = np.random.default_rng(seed)\n",
    "    t = np.linspace(0, 8, n)\n",
    "    freq = np.where(t < 4, 2.0, 4.5) if hopping else np.full_like(t, 2.0)\n",
    "    phase = np.cumsum(freq) * (t[1]-t[0]) * 2*np.pi\n",
    "    clean = np.sin(phase)\n",
    "    y = clean + rng.normal(0, noise, size=n)\n",
    "    return t, clean, y\n",
    "\n",
    "\n",
    "def exponential_smooth(y, alpha):\n",
    "    out = np.zeros_like(y)\n",
    "    out[0] = y[0]\n",
    "    for i in range(1, len(y)):\n",
    "        out[i] = alpha * y[i] + (1-alpha) * out[i-1]\n",
    "    return out\n",
    "\n",
    "\n",
    "def plot_filters(hopping=True, noise=0.5):\n",
    "    t, clean, y = make_signal(hopping=hopping, noise=noise, seed=2)\n",
