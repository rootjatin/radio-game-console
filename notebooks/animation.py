    "def selected_interference_score(selected_channel: int, beacon_channel: int, decoy_channel: int, level: int, phase: float) -> int:\n",
    "    if not interference_active(level):\n",
    "        return 72\n",
    "    sel_bin = channel_to_bin(selected_channel)\n",
    "    dist = abs(selected_channel - beacon_channel)\n",
    "    dist = min(dist, MAX_CHANNEL - dist)\n",
    "    s = interference_score_for_bin(sel_bin, phase, beacon_channel, decoy_channel, level)\n",
    "\n",
    "    # Correct channel lets player exploit the antinode more sharply.\n",
    "    if dist == 0:\n",
    "        s += 10\n",
    "    elif dist <= 1:\n",
    "        s += 4\n",
    "    elif sel_bin != channel_to_bin(beacon_channel):\n",
    "        s -= 14\n",
    "\n",
    "    f = flags_for_level(level)\n",
    "    if f.ghost and sel_bin == channel_to_bin(decoy_channel):\n",
    "        s -= 22\n",
    "    if f.twin and sel_bin == channel_to_bin((beacon_channel + 31) % MAX_CHANNEL):\n",
    "        s -= 16\n",
    "    return int(np.clip(s, 0, 100))\n",
    "\n",
    "\n",
    "def selected_filter_score(level: int, selected_filter: int, selected_channel: int, beacon_channel: int, decoy_channel: int, phase: float, wifi_pressure=0, ble_pressure=0, ghost_here=False) -> int:\n",
    "    \"\"\"Python version of the FAST/MED/SLOW scoring idea.\"\"\"\n",
    "    f = flags_for_level(level)\n",
    "    noise = wifi_pressure + ble_pressure + (2 if ghost_here else 0)\n",
    "    s = 9\n",
    "    wave = selected_interference_score(selected_channel, beacon_channel, decoy_channel, level, phase)\n",
    "\n",
    "    if level <= 2:\n",
    "        s += 5 if selected_filter == 1 else 2\n",
    "    elif selected_filter == 0:  # FAST\n",
    "        s += 7 if f.hops else 1\n",
    "        if noise >= 4:\n",
    "            s -= 4\n",
    "        if wave >= 78:\n",
    "            s += 2\n",
    "    elif selected_filter == 1:  # MED\n",
    "        s += 4\n",
    "        if f.twin:\n",
    "            s -= 2\n",
    "    else:  # SLOW\n",
    "        s += 7 if (f.wifi or noise >= 4) else 1\n",
    "        if f.hops and wave < 60:\n",
    "            s -= 4\n",
    "\n",
    "    return int(np.clip(s, 0, 16))\n",
    "\n",
    "\n",
    "def compute_lock_score(\n",
    "    level:int,\n",
    "    selected_channel:int,\n",
    "    beacon_channel:int,\n",
    "    selected_rate:int,\n",
    "    beacon_rate:int,\n",
    "    selected_power:int,\n",
    "    power_need:int,\n",
    "    selected_filter:int,\n",
    "    phase:float,\n",
    "    decoy_channel:int,\n",
    "    trace:int=0,\n",
    "    wifi_pressure:int=0,\n",
    "    ble_pressure:int=0,\n",
    ") -> int:\n",
    "    \"\"\"Game-style lock score. This is not exact firmware, but follows the same design logic.\"\"\"\n",
    "    f = flags_for_level(level)\n",
    "    score = 0\n",
    "\n",
    "    dist = abs(selected_channel - beacon_channel)\n",
    "    dist = min(dist, MAX_CHANNEL - dist)\n",
    "    if dist == 0:\n",
    "        score += 40\n",
    "    elif dist <= 1:\n",
    "        score += 28\n",
    "    elif channel_to_bin(selected_channel) == channel_to_bin(beacon_channel):\n",
    "        score += 16\n",
    "\n",
    "    if (not f.rate_matters) or selected_rate == beacon_rate:\n",
    "        score += 22\n",
    "    else:\n",
    "        score += 8 if abs(selected_rate - beacon_rate) == 1 else 2\n",
    "\n",
    "    if not f.power_matters:\n",
    "        score += 14\n",
    "    else:\n",
    "        pd = abs(selected_power - power_need)\n",
    "        score += 16 if pd == 0 else (9 if pd == 1 else 3)\n",
    "\n",
    "    # Sample timing score: phase windows represent pulse timing.\n",
    "    pulse_phase = (phase % TWO_PI) / TWO_PI\n",
    "    score += 12 if pulse_phase < 0.25 or pulse_phase > 0.80 else 5\n",
    "\n",
    "    wave_score = selected_interference_score(selected_channel, beacon_channel, decoy_channel, level, phase)\n",
    "    ghost_here = channel_to_bin(selected_channel) == channel_to_bin(decoy_channel) and f.ghost\n",
    "    filt_score = selected_filter_score(level, selected_filter, selected_channel, beacon_channel, decoy_channel, phase, wifi_pressure, ble_pressure, ghost_here)\n",
    "\n",
    "    if not interference_active(level):\n",
